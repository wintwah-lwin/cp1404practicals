#1. ValueError will occur when the input entered is not an integer value.

#2. ZeroDivisionError will occur when the input value for denominator is 0.

#3. Use while loop to ask the user for denominator value until it is not 0.
try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero.")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
print("Finished.")